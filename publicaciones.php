			Axonometrías exactas con Autocad</a>

		<dd>Trabajo presentado en el Segundo Congreso Internacional y Quinto Congreso Nacional de Expresión Grafica en Ingeniería y Arquitectura.
		<h4>RESUMEN</h4>
		<dd>Los programas CAD disponen de herramientas para obtener isometrías a partir de objetos 3D. Otra clase de axonometrías se alcanzan de modo aproximado, por rotación espacial del objeto.
		<dd>Este trabajo presenta una técnica para obtener axonometrías que respondan con exactitud a los coeficientes de reducción o escalas axonométricas que se establezcan.
		<dd>A partir de un modelo sólido y pulsando el botón adecuado se obtiene inmediatemente una isometría, SE, SO, NE, NO.  Se sabe, sin embargo, que estas no son suficientes para atender las necesidades de visualizar la forma de un objeto.
		<dd>Se pueden obtener otras axonometrías modificando el punto de vista. El problema que se intenta resolver con este trabajo es determinar el punto de vista para obtener una axonometría que responda con exactitud a especificaciones de coeficientes de reducción o escalas axonométricas. 
		<dd>El problema se resuelve en primer lugar gráficamente y luego se realiza el mismo cálculo por el camino analítico, lo que permite verificar los resultados obtenidos en el proceso anterior.
		<dd>El cálculo analítico se resuelve mediante una planilla que ha sido organizada de modo tal que permite determinar las coordenadas del punto de vista para cualquier escala axonométrica.
		<dd>Finalmente se presenta una tabla donde se muestran las coordenadas del punto de vista a utilizar para conseguir las axonometrías habituales.
		<dd>Con esta técnica se dispone de una herramienta absolutamente necesaria cuando se requieren axonometrías precisas. Siendo fácil de acceder y de simple aplicación, permitirá optimizar resultados y tiempos de trabajo de ingenieros, arquitectos y diseñadores.
		<dd>Los fundamentos de ésta técnica se pueden verificar en 
